//VARIABLES
//VAR YA NO SE DEBE REALIZAR O UTILIZAR
var helloWorld = "Hola javaScript"
console.log(helloWorld) 

var helloWorld = "Hola DENUEVO, javaScript 2"
console.log(helloWorld) //redeclara la variable

//let
let helloWorld2 = "Hola javaScript 2"
console.log(helloWorld2) //no se puede redeclarar la variable

helloWorld2 = "Hola DENUEVO, javaScript 2"
console.log(helloWorld2) //se puede reasignar el valor de la variable


//const
const helloWorld3 = "Hola javaScript 3"
console.log(helloWorld3) //no se puede redeclarar la variable
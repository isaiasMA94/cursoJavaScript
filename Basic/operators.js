//operadores aritméticos

let a = 5
let b = 10

console.log(a + b); //suma
console.log(a - b); //resta
console.log(a * b); //multiplicacion
console.log(a / b); //division

console.log(a % b); //modulo
console.log(a ** b); //exponencial

a++ //incremento
console.log(a); 
console.log(a++); //incremento postfijo

b-- //decremento
console.log(b); 
console.log(a++); //incremento postfijo


